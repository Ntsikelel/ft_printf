#include "ft_printf.h"

void	ft_get_space(const char *str, int *ind)
{
	if (str[*ind + 1] == 32)
	{
		while (str[*ind + 1] == 32)
			*ind = *ind + 1;
		g_padd->space = TRUE;
	}
}
